#ifndef _lstd_types_h_
#define _lstd_types_h_

/*defining the 8 bits characters*/

typedef unsigned char u8_t;
typedef signed char s8_t;


/*defining the 16 bits int*/

typedef unsigned short int u16_t;
typedef signed short int s16_t;

/*defining the 32 bits int*/

typedef unsigned long int u32_t;
typedef signed long int s32_t;

/*defining the 32 float float*/

typedef  float f32_t;

/*defining the 64 long */

typedef double d64_t;

#endif